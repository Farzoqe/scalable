namespace Farzqe\Scalable;

/**
 * Description of Option
 *
 * @author ZOQ
 */
class Option {

    public $name;
    public $value;

    public function getName() {
        return $this->name;
    }

    public function getValue() {
        return $this->value;
    }

    public function setName($name) {
        $this->name = $name;
        return $this;
    }

    public function setValue($value) {
        $this->value = $value;
        return $this;
    }

}
